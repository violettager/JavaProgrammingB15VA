package day17_while_do_while_loop;

public class DoWhileLoop {
    public static void main(String[] args) {
        int n = 0;

        do{
            System.out.print(n+" ");
            n++;

        }while(n<=10);
        System.out.println();

        int n1 = 0;

        do{
            System.out.print(n1+" ");
            n1+=2;

        }while(n1<=100);



    }
}
