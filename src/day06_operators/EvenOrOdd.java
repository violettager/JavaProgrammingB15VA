package day06_operators;

public class EvenOrOdd {
    public static void main(String[] args){
        System.out.println(4 % 2);//0-4 is even
        System.out.println(5 % 2);//1-5 is odd
        System.out.println(1000%2);//0-1000 is even
        int num = 55;
        System.out.println(num%2); //1-55 is odd
        int num2 = 68;
        System.out.println(num2%2);
    }

}
